package cy.study.algorithm.baekjoon.calculatorio;

public class DogPrint {
  public static void main(String[] arg) {
    // 아래 강아지 모양 출력
    // |\_/|
    // |q p|   /}
    // ( 0 )"""\
    // |"^"`    |
    // ||_/=\\__|
    // 역슬래시를 출력할 땐, 두 번 작성
    // " (따옴표) 출력할 때, 역슬래시
    System.out.println("|\\_/|");
    System.out.println("|q p|   /}");
    System.out.println("( 0 )\"\"\"\\");
    System.out.println("|\"^\"`    |");
    System.out.println("||_/=\\\\__|");
  }
}
