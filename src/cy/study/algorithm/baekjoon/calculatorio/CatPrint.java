package cy.study.algorithm.baekjoon.calculatorio;

public class CatPrint {
  public static void main(String[] arg) {
    // 아래 토끼 모양 출력
    //    \    /\
    //    )  ( ')
    //    (  /  )
    //    \(__)|
    // 역슬래시를 출력할 땐, 두 번 작성
    System.out.println("\\    /\\");
    System.out.println(")  ( ')");
    System.out.println("(  /  )");
    System.out.println("\\(__)|");
  }
}
